#include<iostream>
using namespace std;

int main(){
	int T;
	cin>>T;
	
	while(T--){
		int A,B,C;
		cin>>A>>B>>C;
		
		if(A==7||B==7||C==7){
			cout<<"YES"<<endl;
		}
		else{
			cout<<"NO"<<endl;
		}
 }
	
	return 0;
}
